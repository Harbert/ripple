             <p>The augmented destruction of nature has steered Ripples Foundation to carrying out ecological protection in communities through educating and sensitizing people about the use and value of the environment to us, this has been carried out through community talks, door to door sensitization thus making people in communities more knowledgeable about our environment. Ripples Foundation will begin planting trees and maintaining nature in communities so as to sustain our ecological systems and also improve our quality of life using nature that can be enjoyed now and in the future.</p>
             <p>Another challenge in Ugandan communities is lack of affordable housing, Ripples Foundation has begun helping the less fortunate get shelter and a livable environment through renovating their old homes and also constructing new homes for those who don’t have through using affordable building techniques that only require local building materials and are sustainable in design.</p>
             <p>Poor Sewage and Storm water management in communities has led to the constant floods during rainy seasons that disrupt people’s lives and businesses. Ripples Foundation is carrying out improvement of sewage management in communities through cleaning/creating drainage channels, water harvesting ideas, storm water management and improving accessibility to clean water.</p>
             <p>Ripples Foundation is promoting the 3 R’s (Reduce, Recycle, Reuse) in communities that is, to reduce on the amount of plastics/non-biodegradable materials we use, recycle waste so as to reduce the waste amount in the environment, reuse of materials rather than dumping it away to redeem our environment. </p>

